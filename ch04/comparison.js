let a = 10;
let b = 25;

console.log(a < b); // false
console.log(a === b); // false
console.log(a < b); // true

a = "A";
b = "B"

console.log(a > b); // false
console.log(a === b); // false
console.log(a < b); // true


// 비교 연산자
// == , === -> 일치 비교
// !=, !== -> 불일치 비교
// >, <, >=, <=, -> 크기 비교

